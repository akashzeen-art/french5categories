export const getVideos = () => {
  const list = [];
  const categories = ["cooking", "comedy", "cartoon", "games", "fashion"];
  let videoIndex = 0;

  categories.forEach((cat) => {
    const data = categoryData[cat];
    for (let i = 0; i < data.titles.length; i++) {
      const videoUrl = data.videoUrls
        ? data.videoUrls[i]
        : VIDEO_TEMPLATES[videoIndex % VIDEO_TEMPLATES.length];
      const creatorList = creators[cat];
      const creator = creatorList[i % creatorList.length];
      const viewsCount = Math.floor(Math.random() * 800 + 100);
      const likesCount = Math.floor(viewsCount * 0.15 + 10);
      
      list.push({
        id: `${cat}-${i + 1}`,
        title: data.titles[i],
        description: data.descriptions[i],
        duration: `0${Math.floor(Math.random() * 6 + 3)}:${Math.floor(Math.random() * 40 + 10)}`,
        category: cat,
        thumbnail: data.thumbnails[i],
        videoUrl: videoUrl,
        views: `${viewsCount}K views`,
        likes: `${likesCount}K`,
        released: `${i + 1} day${i === 0 ? "" : "s"} ago`,
        creator: creator,
        tags: [cat, "exclusive", "premium", data.titles[i].split(" ").pop().toLowerCase()],
        comments: []
      });
      videoIndex++;
    }
  });

  return list;
};
